package com.cass.demo.module.parts;

public class PartsDto {

	private String partSeq;
	private String partTypeCd;
	private String partName;
	private Integer partPrice;
	private String partRegDate;
	private String partModDate;
	private int partUseNy;
	private int partDelNy;
	private String partDesc;
	
	public String getPartSeq() {
		return partSeq;
	}
	public void setPartSeq(String partSeq) {
		this.partSeq = partSeq;
	}
	public String getPartTypeCd() {
		return partTypeCd;
	}
	public void setPartTypeCd(String partTypeCd) {
		this.partTypeCd = partTypeCd;
	}
	public String getPartName() {
		return partName;
	}
	public void setPartName(String partName) {
		this.partName = partName;
	}
	public Integer getPartPrice() {
		return partPrice;
	}
	public void setPartPrice(Integer partPrice) {
		this.partPrice = partPrice;
	}
	public String getPartRegDate() {
		return partRegDate;
	}
	public void setPartRegDate(String partRegDate) {
		this.partRegDate = partRegDate;
	}
	public String getPartModDate() {
		return partModDate;
	}
	public void setPartModDate(String partModDate) {
		this.partModDate = partModDate;
	}
	public int getPartUseNy() {
		return partUseNy;
	}
	public void setPartUseNy(int partUseNy) {
		this.partUseNy = partUseNy;
	}
	public int getPartDelNy() {
		return partDelNy;
	}
	public void setPartDelNy(int partDelNy) {
		this.partDelNy = partDelNy;
	}
	public String getPartDesc() {
		return partDesc;
	}
	public void setPartDesc(String partDesc) {
		this.partDesc = partDesc;
	}
}
